One of the lessons it took me a long time to learn is that serious things do not imply seriousness.
As my training progressed, from filling notebooks in my childhood classes or in the rugged wilderness
of camps to the (rather curious) academic requests and the planned writing of these adventures,
I've come to see the human connection that comes through sharing humour is a thousandfold more powerful
than word juggling, as impressive as an author makes them. It is through humour that I am able to reach the author of a book,
to understand our commonality. It is with this in mind that I write my stories now. It is my attempt to allow you to understand the joy and love I feel for the created universe,
as I've understood the joy and love of other authors in their own world.
